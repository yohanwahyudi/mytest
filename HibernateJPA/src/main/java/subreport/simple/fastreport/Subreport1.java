package subreport.simple.fastreport;

public class Subreport1 {
	
	private String ref;
	private String title;
	
	public Subreport1(String ref, String title) {
		this.ref = ref;
		this.title = title;
	}
	
	public String getRef() {
		return ref;
	}
	public void setRef(String ref) {
		this.ref = ref;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	

}
