package subreport.simple;

public class DummyLevel3 {
	
	private String name;
	private Long number;

	public DummyLevel3(String name, Long number) {
		super();
		this.name = name;
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getNumber() {
		return number;
	}
	public void setNumber(Long number) {
		this.number = number;
	}


}
